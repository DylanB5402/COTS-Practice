package org.usfirst.frc.team687.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	public static int DriveVictor1 = 1;
	public static int DriveVictor2 = 2;
	
	public static int DriveVictor3 = 4;
	public static int DriveVictor4 = 5;
	
	public static int ShooterSolenoid1 = 0;
	public static int ShooterSolenoid2 = 1;
	
	public static int FlywheelVictor1 = 3;
	public static int FlywheelVictor2 = 6;
	public static int ArticVictor = 7;
	
}
